long	ft_atoi(const char *str)
{
	long	ng;
	long	res;

	ng = 1;
	res = 0;
	while (*str == ' ' || (*str >= 9 && *str <= 13))
		str ++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			ng = -ng;
		str ++;
	}
	while (*str >= '0' && *str <= '9')
	{
		res *= 10;
		res += *str - '0';
		str ++;
	}
	return (res * ng);
}
